package Entities;


public class Reply {
	private String replyText;
	private int replyID;
	
	
	public void setReplyText(String replyText) {
		this.replyText = replyText;
	}
	public String getReplyText() {
		return replyText;
	}
	public void setReplyID(int replyID) {
		this.replyID = replyID;
	}
	public int getReplyID() {
		return replyID;
	}
}
